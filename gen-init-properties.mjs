#!/usr/bin/env node
/**
 * gen-init-properties.mjs — turn .script-properties.json into a ready-to-run
 * Apps Script file, so setting Script Properties is one function call instead of
 * hand-copying key/value pairs in the editor. Zero dependencies (Node 18+).
 *
 * Script Properties can only be written by code running inside the project (no
 * API sets them). This generates SetupProperties.gs containing an
 * applyScriptProperties() function pre-filled from your local
 * .script-properties.json. Push it, run it once, then DELETE it.
 *
 * USAGE:
 *   node gen-init-properties.mjs                 # .script-properties.json → SetupProperties.gs
 *   node gen-init-properties.mjs --in creds.json --out Setup.gs
 *   node gen-init-properties.mjs --stdout        # print the .gs to stdout instead
 *
 * OPTIONS:
 *   --in <file>    Input JSON (default .script-properties.json)
 *   --out <file>   Output .gs file (default SetupProperties.gs)
 *   --stdout       Write the generated code to stdout; don't touch any file
 *   -h, --help     Show this help
 *
 * ⚠️  The output contains live secrets. It is gitignored by default — do not
 *     commit it, and delete it from the project after running it once.
 */

import { readFileSync, writeFileSync, chmodSync } from 'node:fs';
import { dirname, join, isAbsolute } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const resolvePath = (p) => (isAbsolute(p) ? p : join(HERE, p));
const die = (m) => { console.error(`\x1b[31m✗ ${m}\x1b[0m`); process.exit(1); };
const ok = (m) => console.log(`\x1b[32m✓ ${m}\x1b[0m`);
const warn = (m) => console.log(`\x1b[33m! ${m}\x1b[0m`);

function parseArgs(argv) {
  const out = { in: '.script-properties.json', out: 'SetupProperties.gs', stdout: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    const next = () => argv[++i] ?? die(`${a} needs a value`);
    switch (a) {
      case '--in': out.in = next(); break;
      case '--out': out.out = next(); break;
      case '--stdout': out.stdout = true; break;
      case '-h': case '--help': printHelp(); process.exit(0); break;
      default: die(`Unknown argument: ${a} (try --help)`);
    }
  }
  return out;
}
function printHelp() {
  const src = readFileSync(fileURLToPath(import.meta.url), 'utf8');
  console.log(src.split('\n').filter((l) => l.startsWith(' *') || l.startsWith('/**'))
    .map((l) => l.replace(/^\/\*\*?/, '').replace(/^ \*\/?/, '').replace(/^ ?/, '')).join('\n'));
}

/** Build the SetupProperties.gs source from a properties object. Exported-ish
 *  via a plain function so install-via-api.mjs can reuse it. */
export function buildSetupGs(props) {
  const keys = Object.keys(props);
  if (!keys.length) die('No properties found in the input JSON.');
  // JSON is valid JS object-literal syntax, so this is safe to embed as-is.
  const literal = JSON.stringify(props, null, 2).replace(/\n/g, '\n  ');
  return `/**
 * AUTO-GENERATED by gen-init-properties.mjs — DO NOT COMMIT.
 * Contains live credentials. Run applyScriptProperties() ONCE from the Run
 * menu, confirm the log line, then DELETE this file (and re-push) so the
 * secrets don't linger in the project source.
 */
function applyScriptProperties() {
  var props = ${literal};
  PropertiesService.getScriptProperties().setProperties(props, false); // merge
  Logger.log('Applied ' + Object.keys(props).length +
    ' script propert(ies). Now DELETE SetupProperties.gs and re-push.');
}
`;
}

// Only run the CLI when invoked directly (not when imported).
if (import.meta.url === `file://${process.argv[1]}`) {
  const opts = parseArgs(process.argv.slice(2));
  const inPath = resolvePath(opts.in);
  let props;
  try { props = JSON.parse(readFileSync(inPath, 'utf8')); }
  catch (e) { die(`Could not read ${opts.in}: ${e.message}\n  Run connect-dropbox.mjs first to create it.`); }

  const gs = buildSetupGs(props);

  if (opts.stdout) { process.stdout.write(gs); }
  else {
    const outPath = resolvePath(opts.out);
    writeFileSync(outPath, gs);
    try { chmodSync(outPath, 0o600); } catch { /* non-posix */ }
    ok(`Wrote ${opts.out} (gitignored, chmod 600)`);
    warn('It contains secrets. Push it, run applyScriptProperties() once, then delete it.');
    console.log(`
Next:
  • clasp:   clasp push  →  clasp run applyScriptProperties  (or run it in the editor)
  • REST:    node install-via-api.mjs --with-properties      (pushes it for you)
  • manual:  paste ${opts.out} into the editor, run applyScriptProperties, delete it
`);
  }
}
